class DateTimeUtil {
    today(date){
        console.log(`[INFO] ${date} [INFO]`);
    }
    setYear(date, year){
        console.log(`[INFO] ${date, year} [INFO]`);
    }
    daysDifference(dateLeft, dateRight){
        console.log(`[INFO] ${dateLeft, dateRight} [INFO]`);
    }
}

module.exports = new DateTimeUtil();